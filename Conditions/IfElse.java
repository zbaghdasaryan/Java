public class IfElse {
    public static void main(String[] args) {
        int a = 5, b = 1;

        if (a < b) {
            System.out.println("a < b");
        }
        if(a==b){
            System.out.println("a = b");
        }
        else {
            System.out.println("a > b");
        }
    }
}
